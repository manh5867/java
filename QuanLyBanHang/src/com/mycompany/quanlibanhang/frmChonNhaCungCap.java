    public static void hienThiDanhSachCongTy()
    {
        //Khai báo 1 tiêu đề
        String colTieuDe[] = new String[]{"ID", "Họ tên", "Điện thoại", "Email", "Địa chỉ"};
        
        //Khai báo đối tượng để thiển thị lên table
        DefaultTableModel model = new DefaultTableModel(colTieuDe, 0);
        
        //Lấy danh sách sv
        List<CongTy> lstCongTy = DataProvider.getCongTyBus().layDanhSach();
        
        Object row[] = null;
        
        //Duyệt để đưa vào model
        for(CongTy kh : lstCongTy)
        {
            //Khởi tạo mảng
            row = new Object[5];
            
            row[0] = kh.getID();
            row[1] = kh.getTenCongTy();
            row[2] = kh.getDienThoai();
            row[3] = kh.getEmail();
            row[4] = kh.getDiaChi();
            
            //Thêm vào model
            model.addRow(row);
        }
        
        //Hiển thị lên table
        jTableNhaCungCap.setModel(model);        
    }

