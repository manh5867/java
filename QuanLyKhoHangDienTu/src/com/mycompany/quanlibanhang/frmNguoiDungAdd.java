    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        NguoiDung objKH = new NguoiDung();

        String ID = "", taiKhoan = "", matKhau = "", hoTen = "", quyen = "";

        

        ID = txtID.getText();
        taiKhoan = txtTaiKhoan.getText();
        matKhau = txtMatKhau.getText();
        hoTen = txtHoTen.getText();
        quyen = cbo.getSelectedItem().toString();

        //Gán giá trị cho các thuộc tính
        objKH.setId(ID);
        objKH.setHoTen(hoTen);
        objKH.setMatKhau(matKhau);
        objKH.setTenDangNhap(taiKhoan);
        objKH.setQuyenSuDung(quyen);

        boolean ketQua = false;

        //Khai báo 1 đối tượng
        NguoiDungBusiness sinhVienBus = new NguoiDungBusiness();

        if(!idNguoiDung.isEmpty())//TH sửa
        {
            ketQua = sinhVienBus.capNhat(objKH);
        }
        else
        {
            //Thực hiện thêm mới
            ketQua = sinhVienBus.themMoi(objKH);
        }

        if(ketQua)//true
        {
            JOptionPane.showMessageDialog(rootPane, "Thực hiện cập nhật tài khoản thành công");
            //Reload lại danh sách
            GiaoDienChinh.hienThiDanhSachNguoiDung();
        }

    }//GEN-LAST:event_btnCapNhatActionPerformed

