    private void hienThiChiTiet()
    {
        NguoiDung objKH = DataProvider.getNguoiDungBus().layChiTietTheoMa(idNguoiDung);
        
        if(objKH != null)
        {
            txtID.setText(idNguoiDung);
            txtID.setEditable(false);
            txtHoTen.setText(objKH.getHoTen());
            txtTaiKhoan.setText(objKH.getTenDangNhap());
            txtMatKhau.setText(objKH.getMatKhau());
            cbo.getModel().setSelectedItem(objKH.getQuyenSuDung());
            
            
            
            
        }
        
    }
